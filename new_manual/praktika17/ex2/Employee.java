package ex2;

public class Employee {
    private String name;
    private int fee;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWages() {
        return fee;
    }

    public void setWages(int fee) {
        this.fee = fee;
    }
}